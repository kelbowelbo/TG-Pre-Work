let athleteFinalPosition = 'first place';

switch (athleteFinalPosition) {
 	case "first place":
    console.log("You get the gold medal!");
    break;
  case "second place":
  	console.log("You get the silver medal!");
  	break;
  case "third place":
    console.log("You get the bronze medal!");
    break;
  default:
    console.log("No medal awarded.");
    break;
}

/*In this session, I learned that using the switch keyword can allow you to be much
more efficient when we need to check multiple conditions.*/
